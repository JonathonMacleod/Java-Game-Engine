public class Screen {

	private int[] pixels;
	private int width, height;
	
	public Screen(int width, int height, int[] pixels) {
		assert (width > 0) : "Width of a screen must be above 0";
		assert (height > 0) : "Height of a screen must be above 0";
		assert (pixels != null) : "Pixel array cannot be null";
	}
	
	
	
	
}
